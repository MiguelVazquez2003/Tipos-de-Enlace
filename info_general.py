def mostrar_informacion_general(st):
    st.header("¡Hola! Somos el Equipo 3 🚀")
    st.markdown("""
    En esta pequeña página web, podrás identificar el tipo de enlace químico entre dos elementos.
    ¡Aquí te explicamos brevemente los tipos de enlaces químicos más comunes! 🧪
    """)
    st.header("🔗 Tipos de Enlaces Químicos")
    st.markdown("""
    En química, los enlaces químicos son las fuerzas que mantienen unidos a los átomos en las moléculas y los compuestos. 
    Existen varios tipos de enlaces químicos, pero tenemos que los más comunes son:

    1. **Enlace Iónico** ⚡:
       - Se forma cuando un átomo cede uno o más electrones a otro átomo.
       - Generalmente ocurre entre metales y no metales.
       - Ejemplo: NaCl (cloruro de sodio) 🧂.

    2. **Enlace Covalente** 🔗:
       - Se forma cuando dos átomos comparten uno o más pares de electrones.
       - Puede ser:
         - **Covalente no polar** ⚖️: cuando los electrones se comparten equitativamente.
         - **Covalente polar** ⚡: cuando los electrones se comparten de manera desigual.
       - Ejemplo: H2O (agua) 💧.

    3. **Enlace Metálico** 🛠️:
       - Se forma entre átomos de metales.
       - Los electrones se mueven libremente entre los átomos, creando una "nube" de electrones.
       - Ejemplo: Fe (hierro) 🪨.
    """)

    st.markdown("""
    **Nota**: En este programa solo consideramos dos elementos para determinar el tipo de enlace. 🧬
    """)